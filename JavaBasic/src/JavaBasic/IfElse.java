package JavaBasic;

public class IfElse {

	public static void main(String[] args) 
	{
		int a = 51;
		
		if(a<=5){
			System.out.println("A is a small no.");
		}
		else if(a>5 && a<=50){
			System.out.println("A is medium size no.");
		}
		else{
			System.out.println("A is largest no.");
		}
		

	}

}
